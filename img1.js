
// Function to set image dimensions
function pequena1() {
    img = document.getElementById("img1");
    img.style.width = "100%";
    img.style.height = "auto";
    img.style.transition = "width 0.5s ease";
}
// Function to reset image dimensions
function grande1() {
    img = document.getElementById("img1");
    img.style.width = "150%";
    img.style.height = "auto";
    img.style.transition = "width 0.5s ease";
}
